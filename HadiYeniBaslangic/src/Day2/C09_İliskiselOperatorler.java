package Day2;

public class C09_İliskiselOperatorler {
    public static void main(String[] args) {
        // < > >= <= == != iliskisel opartorler genelikle sartlı ifadelerde kullanılır

        int sayi1= 100;
        int sayi2=90;

        System.out.println(sayi1>sayi2);// true
        System.out.println(sayi1<sayi2); // false
        System.out.println(sayi1>=sayi2);//true
        System.out.println(sayi1<=sayi2);//false
        System.out.println(sayi1==sayi2);//false
        System.out.println(sayi1!=sayi2);//true


    }
}
